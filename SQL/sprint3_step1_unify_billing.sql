-- ============================================================
-- STEP 2: EXTEND fee_dues FOR MANUAL BILLING
-- ============================================================

-- 2a. Add source column — 'structured' (auto-generated) or 'manual' (admin created)
ALTER TABLE public.fee_dues
  ADD COLUMN IF NOT EXISTS source TEXT NOT NULL DEFAULT 'structured'
    CHECK (source IN ('structured', 'manual'));

-- 2b. Make fee_structure_id nullable — manual dues have no structure
ALTER TABLE public.fee_dues
  ALTER COLUMN fee_structure_id DROP NOT NULL;

-- 2c. Make fee_structure_item_id nullable — manual dues have no item
ALTER TABLE public.fee_dues
  ALTER COLUMN fee_structure_item_id DROP NOT NULL;

-- 2d. Expand fee_type check — add hostel, admission, other already exist but
--     also add 'miscellaneous' as a catch-all for admin
ALTER TABLE public.fee_dues
  DROP CONSTRAINT IF EXISTS fee_dues_fee_type_check;

-- No CHECK on fee_type — let it be freeform text for manual dues
-- Structured dues use the item's fee_type which is already validated at structure level

-- 2e. DROP the old unique constraint that required fee_structure_item_id
--     (it would block manual dues which have NULL fee_structure_item_id)
ALTER TABLE public.fee_dues
  DROP CONSTRAINT IF EXISTS fee_dues_unique_per_month;

-- 2f. ADD partial unique constraint: only one structured due per student/item/month
--     Manual dues (source='manual') are exempt — admin can add as many as needed
CREATE UNIQUE INDEX IF NOT EXISTS fee_dues_structured_unique
  ON public.fee_dues (student_id, fee_structure_item_id, month)
  WHERE source = 'structured' AND fee_structure_item_id IS NOT NULL;

-- 2g. Add label column for manual dues — freeform admin description
--     (label column already exists, we just ensure it accepts manual values)
--     Add academic_year default for manual dues (current school year)
--     No schema change needed — label is already TEXT NOT NULL

-- 2h. Update table comment to reflect unified purpose
COMMENT ON TABLE public.fee_dues IS
  'Unified fee dues table. source=''structured'' for auto-generated monthly dues from fee_structures. '
  'source=''manual'' for admin-created ad-hoc dues (replaces the old fees table). '
  'balance is GENERATED ALWAYS AS (total_due - amount_paid) — never update directly. '
  'record_fee_payment() is the only way to record payments.';

COMMENT ON COLUMN public.fee_dues.source IS
  '''structured'' = auto-generated by generate_fee_dues_capped(). '
  '''manual'' = created by admin via create_manual_due() RPC.';


-- ============================================================
-- STEP 3: create_manual_due() — NEW RPC
-- Admin creates an ad-hoc fee due for a student.
-- Optionally collects payment immediately (at billing time).
-- Multiple fee types supported — call once per line item.
-- ============================================================

CREATE OR REPLACE FUNCTION public.create_manual_due(
  p_school_id      UUID,
  p_student_id     UUID,
  p_fee_type       TEXT,         -- e.g. 'tuition', 'exam', 'transport', 'hostel', 'admission', 'other'
  p_label          TEXT,         -- display label e.g. 'Tuition Fee - June 2026'
  p_amount         NUMERIC(10,2),
  p_due_date       DATE,
  p_month          TEXT,         -- 'YYYY-MM' e.g. '2026-06'
  p_academic_year  TEXT,         -- e.g. '2025-26'
  p_notes          TEXT DEFAULT NULL
)
RETURNS TABLE (due_id UUID)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_due_id  UUID;
BEGIN

  -- ── Auth: must be active school_admin for this school ──
  IF NOT EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id        = auth.uid()
      AND school_id = p_school_id
      AND role      = 'school_admin'
      AND is_active = true
  ) THEN
    RAISE EXCEPTION 'Access denied — school_admin role required';
  END IF;

  -- ── Validate student belongs to this school ──
  IF NOT EXISTS (
    SELECT 1 FROM public.students
    WHERE id        = p_student_id
      AND school_id = p_school_id
      AND is_active = true
  ) THEN
    RAISE EXCEPTION 'Student not found or not active in this school';
  END IF;

  -- ── Validate amount ──
  IF p_amount IS NULL OR p_amount <= 0 THEN
    RAISE EXCEPTION 'Amount must be greater than zero';
  END IF;

  -- ── Insert manual due ──
  INSERT INTO public.fee_dues (
    school_id,
    student_id,
    fee_structure_id,       -- NULL for manual
    fee_structure_item_id,  -- NULL for manual
    source,
    fee_type,
    label,
    month,
    academic_year,
    due_date,
    base_amount,
    discount_amount,
    net_amount,
    late_fee_amount,
    total_due,
    amount_paid,
    status,
    notes
  ) VALUES (
    p_school_id,
    p_student_id,
    NULL,                   -- no structure
    NULL,                   -- no structure item
    'manual',
    p_fee_type,
    p_label,
    p_month,
    p_academic_year,
    p_due_date,
    p_amount,               -- base = full amount (no discount on manual)
    0,                      -- no discount
    p_amount,               -- net = base
    0,                      -- no late fee at creation
    p_amount,               -- total_due = amount
    0,                      -- amount_paid starts at 0
    'unpaid',
    p_notes
  )
  RETURNING id INTO v_due_id;

  RETURN QUERY SELECT v_due_id;

END;
$$;

-- Permissions
REVOKE ALL ON FUNCTION public.create_manual_due(
  UUID, UUID, TEXT, TEXT, NUMERIC, DATE, TEXT, TEXT, TEXT
) FROM PUBLIC;

GRANT EXECUTE ON FUNCTION public.create_manual_due(
  UUID, UUID, TEXT, TEXT, NUMERIC, DATE, TEXT, TEXT, TEXT
) TO authenticated;


-- ============================================================
-- STEP 4: REBUILD get_student_fee_ledger()
-- Remove all references to the fees table.
-- Manual dues now come from fee_dues WHERE source = 'manual'.
-- ============================================================

CREATE OR REPLACE FUNCTION public.get_student_fee_ledger(
  p_school_id  UUID,
  p_student_id UUID
)
RETURNS TABLE (
  due_id             UUID,
  source             TEXT,
  fee_type           TEXT,
  label              TEXT,
  month              TEXT,
  academic_year      TEXT,
  due_date           DATE,
  base_amount        NUMERIC,
  discount_amount    NUMERIC,
  net_amount         NUMERIC,
  late_fee_amount    NUMERIC,
  total_due          NUMERIC,
  amount_paid        NUMERIC,
  balance            NUMERIC,
  status             TEXT,
  late_fee_applied   BOOLEAN,
  payments_count     BIGINT,
  last_payment_date  DATE,
  last_receipt       TEXT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN

  -- ── Auth: school_admin only ──
  IF NOT EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id        = auth.uid()
      AND school_id = p_school_id
      AND role      = 'school_admin'
      AND is_active = true
  ) THEN
    RAISE EXCEPTION 'Access denied — school_admin role required';
  END IF;

  RETURN QUERY
  SELECT
    d.id                                        AS due_id,
    d.source,
    d.fee_type,
    d.label,
    d.month,
    d.academic_year,
    d.due_date,
    d.base_amount,
    d.discount_amount,
    d.net_amount,
    d.late_fee_amount,
    d.total_due,
    d.amount_paid,
    d.balance,
    d.status,
    d.late_fee_applied,
    COUNT(fp.id)                                AS payments_count,
    MAX(fp.paid_date)                           AS last_payment_date,
    MAX(fp.receipt_number)                      AS last_receipt
  FROM public.fee_dues d
  LEFT JOIN public.fee_payments fp ON fp.fee_due_id = d.id
  WHERE d.school_id  = p_school_id
    AND d.student_id = p_student_id
  GROUP BY
    d.id, d.source, d.fee_type, d.label, d.month,
    d.academic_year, d.due_date, d.base_amount,
    d.discount_amount, d.net_amount, d.late_fee_amount,
    d.total_due, d.amount_paid, d.balance,
    d.status, d.late_fee_applied
  ORDER BY d.due_date DESC, d.created_at DESC;

END;
$$;

-- Permissions
REVOKE ALL ON FUNCTION public.get_student_fee_ledger(UUID, UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.get_student_fee_ledger(UUID, UUID) TO authenticated;


-- ============================================================
-- STEP 5: REBUILD get_fee_dashboard_stats()
-- Remove fees table queries — everything from fee_payments now.
-- ============================================================

CREATE OR REPLACE FUNCTION public.get_fee_dashboard_stats(p_school_id UUID)
RETURNS TABLE (
  today_collection    NUMERIC,
  month_collection    NUMERIC,
  total_pending       NUMERIC,
  defaulters_count    BIGINT,
  total_collected_ytd NUMERIC
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN
  RETURN QUERY
  SELECT
    -- Today's total collection (all sources via fee_payments)
    COALESCE((
      SELECT SUM(fp.amount_paid)
      FROM public.fee_payments fp
      WHERE fp.school_id = p_school_id
        AND fp.paid_date = CURRENT_DATE
    ), 0::NUMERIC),

    -- This calendar month's total collection
    COALESCE((
      SELECT SUM(fp.amount_paid)
      FROM public.fee_payments fp
      WHERE fp.school_id = p_school_id
        AND TO_CHAR(fp.paid_date, 'YYYY-MM') = TO_CHAR(CURRENT_DATE, 'YYYY-MM')
    ), 0::NUMERIC),

    -- Total outstanding balance — ALL dues (structured + manual)
    COALESCE((
      SELECT SUM(d.balance)
      FROM public.fee_dues d
      WHERE d.school_id  = p_school_id
        AND d.status     IN ('unpaid', 'partial')
        AND d.balance    > 0
    ), 0::NUMERIC),

    -- Defaulters count — students with any unpaid/partial due
    COALESCE((
      SELECT COUNT(DISTINCT d.student_id)
      FROM public.fee_dues d
      WHERE d.school_id  = p_school_id
        AND d.status     IN ('unpaid', 'partial')
        AND d.balance    > 0
        AND d.due_date   < CURRENT_DATE
    ), 0::BIGINT),

    -- Year-to-date collection (calendar year)
    COALESCE((
      SELECT SUM(fp.amount_paid)
      FROM public.fee_payments fp
      WHERE fp.school_id = p_school_id
        AND EXTRACT(YEAR FROM fp.paid_date) = EXTRACT(YEAR FROM CURRENT_DATE)
    ), 0::NUMERIC);

END;
$$;

-- Permissions
REVOKE ALL ON FUNCTION public.get_fee_dashboard_stats(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.get_fee_dashboard_stats(UUID) TO authenticated;


-- ============================================================
-- STEP 6: REBUILD get_student_billing_summary()
-- Already only queries fee_dues — just add source to the return.
-- ============================================================

CREATE OR REPLACE FUNCTION public.get_student_billing_summary(
  p_student_id UUID
)
RETURNS TABLE (
  student_id          UUID,
  full_name           TEXT,
  student_uid         TEXT,
  father_name         TEXT,
  parent_phone        TEXT,
  class_name          TEXT,
  class_section       TEXT,
  fee_structure_id    UUID,
  fee_structure_name  TEXT,
  source              TEXT,
  due_id              UUID,
  due_label           TEXT,
  due_month           TEXT,
  due_date            DATE,
  total_due           NUMERIC,
  amount_paid         NUMERIC,
  balance             NUMERIC,
  status              TEXT,
  late_fee_applied    BOOLEAN,
  grand_total_due     NUMERIC,
  grand_total_paid    NUMERIC,
  grand_balance       NUMERIC
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
DECLARE
  v_school_id UUID;
BEGIN

  -- ── Get school_id from calling admin's profile ──
  SELECT p.school_id INTO v_school_id
  FROM public.profiles p
  WHERE p.id        = auth.uid()
    AND p.role      = 'school_admin'
    AND p.is_active = true;

  IF v_school_id IS NULL THEN
    RAISE EXCEPTION 'Access denied — school_admin role required';
  END IF;

  RETURN QUERY
  SELECT
    s.id                                          AS student_id,
    s.full_name,
    s.student_uid,
    s.father_name,
    s.parent_phone,
    c.name                                        AS class_name,
    c.section                                     AS class_section,
    d.fee_structure_id,
    fs.name                                       AS fee_structure_name,
    d.source,
    d.id                                          AS due_id,
    d.label                                       AS due_label,
    d.month                                       AS due_month,
    d.due_date,
    d.total_due,
    d.amount_paid,
    d.balance,
    d.status,
    d.late_fee_applied,
    -- Grand totals as window aggregates
    SUM(CASE WHEN d.status IN ('unpaid','partial','paid') THEN d.total_due  ELSE 0 END)
      OVER (PARTITION BY s.id)                    AS grand_total_due,
    SUM(d.amount_paid)
      OVER (PARTITION BY s.id)                    AS grand_total_paid,
    SUM(CASE WHEN d.status IN ('unpaid','partial') THEN d.balance ELSE 0 END)
      OVER (PARTITION BY s.id)                    AS grand_balance
  FROM public.students s
  JOIN public.fee_dues d      ON d.student_id = s.id
  LEFT JOIN public.classes c  ON c.id         = s.class_id
  LEFT JOIN public.fee_structures fs ON fs.id = d.fee_structure_id
  WHERE s.id         = p_student_id
    AND d.school_id  = v_school_id
    AND d.status    IN ('unpaid', 'partial')
    AND d.balance    > 0
  ORDER BY d.due_date ASC, d.source ASC, d.created_at ASC;

END;
$$;

-- Permissions
REVOKE ALL ON FUNCTION public.get_student_billing_summary(UUID) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.get_student_billing_summary(UUID) TO authenticated;


