-- STEP 7: UPDATE get_defaulters() — add source awareness
-- Already queries only fee_dues — just ensure manual dues included.
-- (No schema change needed — WHERE clause already covers all sources)
-- Re-run to ensure it's the canonical deployed version.
-- ============================================================

CREATE OR REPLACE FUNCTION public.get_defaulters(
  p_school_id     UUID,
  p_class_id      UUID DEFAULT NULL,
  p_academic_year TEXT DEFAULT NULL
)
RETURNS TABLE (
  student_id      UUID,
  full_name       TEXT,
  student_uid     TEXT,
  class_name      TEXT,
  class_section   TEXT,
  total_balance   NUMERIC,
  oldest_due_date DATE,
  days_overdue    INTEGER,
  dues_count      BIGINT
)
LANGUAGE plpgsql
SECURITY DEFINER
SET search_path = public
AS $$
BEGIN

  IF NOT EXISTS (
    SELECT 1 FROM public.profiles
    WHERE id        = auth.uid()
      AND school_id = p_school_id
      AND role      = 'school_admin'
      AND is_active = true
  ) THEN
    RAISE EXCEPTION 'Access denied — school_admin role required';
  END IF;

  RETURN QUERY
  SELECT
    s.id,
    s.full_name,
    s.student_uid,
    c.name                              AS class_name,
    c.section                           AS class_section,
    SUM(d.balance)                      AS total_balance,
    MIN(d.due_date)                     AS oldest_due_date,
    (CURRENT_DATE - MIN(d.due_date))    AS days_overdue,
    COUNT(d.id)                         AS dues_count
  FROM public.students s
  JOIN public.fee_dues d     ON d.student_id = s.id
  LEFT JOIN public.classes c ON c.id         = s.class_id
  WHERE d.school_id   = p_school_id
    AND d.status      IN ('unpaid', 'partial')
    AND d.balance     > 0
    AND d.due_date    < CURRENT_DATE
    AND (p_class_id      IS NULL OR s.class_id      = p_class_id)
    AND (p_academic_year IS NULL OR d.academic_year = p_academic_year)
  GROUP BY s.id, s.full_name, s.student_uid, c.name, c.section
  ORDER BY total_balance DESC;

END;
$$;

REVOKE ALL ON FUNCTION public.get_defaulters(UUID, UUID, TEXT) FROM PUBLIC;
GRANT EXECUTE ON FUNCTION public.get_defaulters(UUID, UUID, TEXT) TO authenticated;


